public class TiposPrimitivosDeDados {
    // Declaração Inteiros
    private int x = 3;
    private int y = (int) 3;                //typecast
    private Integer z = new Integer(3);     //Wrapper Class = objeto   
    // Declaração float
    private float sal = 1825.54f;
    private float sal2 = (float) 1825.54;
    private Float sal3 = new Float(1825.54);
    // Declaração char 
    private char letra = 'G';
    private char letra2 = (char) 'G';
    private Character letra3 = new Character('G');
    // Declaração Booleano
    private boolean casado = false;
    private boolean casado2 = (boolean) false;
    private Boolean casado3 = new Boolean(false);       
}
