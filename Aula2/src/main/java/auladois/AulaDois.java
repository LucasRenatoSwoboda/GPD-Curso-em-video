/**Compilador Java -> javac
 * Java -> Gera um Bytecode
 * JVM -> Java Virtual Machine:
 * - Interpreta o código e gera um executável para a máquina
 * - Um Bytecode gerado pode ser executado em qualquer plataforma
 * WORA - Write once, run anywhere.
 * 
 * JRE (Java Runtime Environment) é composto de:
 * - JVM
 * -- Loader: Carrega o Bytecode na memória da JVM
 * -- Verificador: Verifica se o código pode ser executável
 * -- Interpretador: Pega o Bytecode e transforma em instrução nativa do SO
 * -- Gerenciador de memória: Gerencia o armazenamento da memória da JVM
 * - Bibliotecas
 * 
 * JDK (Java Devolpment Kit) é composto de:
 * - JRE
 * - JavaLang
 * - JavaTools
 * -- javac: Compilador
 * -- Debugger: Verificar como o programa está sendo executado
 * -- APIs: Bibliotecas para desenvolvimento * 
 */